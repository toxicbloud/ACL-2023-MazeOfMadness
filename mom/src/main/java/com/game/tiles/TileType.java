package com.game.tiles;

/**
 * TileType enum.
 * This is the enum for the tile types.
 */
public enum TileType {
    /** void type. */
    VOID,
    /** Rock wall type. */
    WALL_ROCK,
    /** Rock ground type. */
    GROUND_ROCK,
    /** Water ground type. */
    GROUND_WATER,
    /** Lava ground type. */
    GROUND_LAVA,
    /** Ground Spikes. */
    GROUND_SPIKES,
    /** Grass ground type. */
    GROUND_GRASS,
    /** End ground type. */
    GROUND_END,
    /** Next ground type. */
    GROUND_NEXT,
    /** Stair rock type. */
    STAIR_ROCK,
    /** Stair grass type. */
    STAIR_GRASS
}
