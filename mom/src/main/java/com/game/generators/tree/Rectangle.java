package com.game.generators.tree;

public class Rectangle {

    public int start_x, end_x, start_y, end_y;

    public Rectangle(int start_x, int end_x, int start_y, int end_y){
        this.start_x = start_x;
        this.start_y = start_y;
        this.end_x = end_x;
        this.end_y = end_y;
    }


}
