package com.engine.events;

/**
 * GamepadBtn enum.
 * This is the gamepad button.
 */
public enum GamepadBtn {
    /** A gamepad button. */
    BTN_A,
    /** B gamepad button. */
    BTN_B,
    /** X gamepad button. */
    BTN_X,
    /** Y gamepad button. */
    BTN_Y,
    /** Left bumper gamepad button. */
    BTN_L1,
    /** Right bumper gamepad button. */
    BTN_R1,
    /** Left trigger gamepad button. */
    BTN_L2,
    /** Right trigger gamepad button. */
    BTN_R2,
    /** Left joystick gamepad button. */
    BTN_L3,
    /** Right joystick gamepad button. */
    BTN_R3,
    /** Back gamepad button. */
    BTN_BACK,
    /** Start gamepad button. */
    BTN_START,
    /** D-pad up gamepad button. */
    BTN_DPAD_UP,
    /** D-pad down gamepad button. */
    BTN_DPAD_DOWN,
    /** D-pad left gamepad button. */
    BTN_DPAD_LEFT,
    /** D-pad right gamepad button. */
    BTN_DPAD_RIGHT
}
